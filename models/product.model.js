const mongoose=require('mongoose');

const ProductSchema=mongoose.Schema({
	name:{
		type:String,
		required:true
	},
	description:{
		type:String,
		required:true
	},
	richDescription:{
		type:String,
		default:'',
	},
	image:{
		type:String,
		default:'image.jpg'
	},
	images:[{
		type:String,
	}],
	price:{
		type:Number,
		required:true
	},
	category:{
		type:mongoose.Schema.Types.ObjectId,
		ref:'category',
		required:true
	},
	autor:{
		type:mongoose.Schema.Types.ObjectId,
		ref:'users',
		required:true
	},
	brand:{
		type:String,
		default:''
	},
	countInStock:{
		type:Number,
		required:true,
		min:0,
		max:255
	},
	rating:{
		type:Number,
		default:0
	},
	numReviews:{
		type:Number,
		default:0
	},
	isFeatured:{
		type:Boolean,
		default:false
	},
	dateCreate:{
		type:Date,
		default:Date.now
	}
})

module.exports=mongoose.model('products',ProductSchema);